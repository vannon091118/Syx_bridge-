try {
  const saved = (typeof localStorage !== 'undefined') ? localStorage.getItem('syxbridge-ui-lang') : null;
  if (saved && SUPPORTED_UI_LANGS.includes(saved)) _uiLang = saved;
} catch (e) { /* localStorage not available */ }

function setUILanguage(lang) {
  if (SUPPORTED_UI_LANGS.includes(lang)) {
    _uiLang = lang;
    try { if (typeof localStorage !== 'undefined') localStorage.setItem('syxbridge-ui-lang', lang); } catch (e) {}
    if (typeof window !== 'undefined' && window.dispatchEvent) {
      window.dispatchEvent(new CustomEvent('uilangchange', { detail: { lang } }));
    }
  }
}

// ── Window exports for HTML onclick handlers ─────────────────────────
if (typeof window !== 'undefined') {
  window.t = t;
  window.setUILanguage = setUILanguage;
  window.getUILanguage = getUILanguage;
  window.SUPPORTED_UI_LANGS = SUPPORTED_UI_LANGS;
}

