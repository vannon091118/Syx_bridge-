function t(key, lang) {
  const l = lang || _uiLang;
  const parts = key.split('.');
  let node = STRINGS;
  for (const p of parts) {
    if (!node || !node[p]) return '[[' + key + ']]';
    node = node[p];
  }
  if (typeof node === 'string') return node;
  return node[l] || node['English'] || node['German'] || '[[' + key + ']]';
}

